function randomRange(myMin, myMax) {
    // Generate a random decimal number between 0 (inclusive) and 1 (exclusive)
    const randomDecimal = Math.random();
  
    // Calculate the range of values between myMin and myMax (inclusive)
    const range = myMax - myMin + 1;
  
    // Multiply the random decimal by the range and add myMin to shift the result
    const randomWhole = Math.floor(randomDecimal * range) + myMin;
  
    // Return the random whole number
    return randomWhole;
  }
  
  // Test the function
  console.log(randomRange(5, 10)); // This will print a random whole number between 5 and 10 (inclusive)
  